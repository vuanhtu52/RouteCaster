import UIKit

extension UIViewController{
    func configureNav() {
        navigationController?.navigationBar.barStyle = .black
        navigationController?.navigationBar.barTintColor = UIColor(red: 202/255, green: 111/255, blue: 252/255, alpha: 1)
    }

}
